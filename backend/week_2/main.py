def analyze_profile(payload: dict[str, Any]) -> dict[str, Any]:
    snapshot, benchmark = build_snapshot(payload)
    snapshot["savings_goal"] = safe_float(payload.get("savings_goal"), 0)
    snapshot["goal_months"]  = int(safe_float(payload.get("goal_months"), 6)) or 6

    # ── Single Gemini call — no second call for summary ───────────────────────
    plan = build_plan(snapshot, benchmark, payload)

    # Bring summary up to snapshot so FinancialSnapshot.jsx can read it
    snapshot["summary"] = plan.get("summary", "")
    snapshot["spending_type"] = plan.get("spending_type", "")

    finance_gaps = build_finance_gaps({**snapshot, **payload}, benchmark, plan)
    plan["goal_amount"] = safe_float(payload.get("savings_goal"), 5000)

    pipeline_state = (
        "high-risk"     if any(g["severity"] == "high"   for g in finance_gaps) else
        "moderate-risk" if any(g["severity"] == "medium" for g in finance_gaps) else
        "stable"
    )

    return {
        "pipeline_stage": "finance_gap_detection",
        "pipeline_state": pipeline_state,
        "snapshot":       snapshot,
        "finance_gaps":   finance_gaps,
        "plan":           plan,
    }


class FinancialAnalysisHandler(BaseHTTPRequestHandler):
    def _send_json(self, status_code: int, payload: dict[str, Any]) -> None:
        encoded = json.dumps(payload, ensure_ascii=False).encode("utf-8")
        self.send_response(status_code)
        self.send_header("Content-Type",   "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(encoded)))
        self.send_header("Access-Control-Allow-Origin",  "*")
        self.send_header("Access-Control-Allow-Headers", "Content-Type")
        self.send_header("Access-Control-Allow-Methods", "GET, POST, OPTIONS")
        self.end_headers()
        self.wfile.write(encoded)

    def do_OPTIONS(self) -> None:
        self.send_response(204)
        self.send_header("Access-Control-Allow-Origin",  "*")
        self.send_header("Access-Control-Allow-Headers", "Content-Type")
        self.send_header("Access-Control-Allow-Methods", "GET, POST, OPTIONS")
        self.end_headers()

    def do_GET(self) -> None:
        if self.path in {"/", "/api/health"}:
            self._send_json(200, {"status": "ok", "service": "kyouth finance backend"})
            return
        self._send_json(404, {"error": "Not found"})

    def do_POST(self) -> None:
        if self.path != "/api/analyze":
            self._send_json(404, {"error": "Not found"})
            return

        content_length = int(self.headers.get("Content-Length", "0"))
        raw_body       = self.rfile.read(content_length).decode("utf-8") if content_length else "{}"

        try:
            payload = json.loads(raw_body)
        except json.JSONDecodeError:
            self._send_json(400, {"error": "Invalid JSON payload"})
            return

        # Input validation before touching Gemini
        gross_salary = safe_float(payload.get("gross_salary"))
        if gross_salary <= 0:
            self._send_json(400, {"error": "gross_salary must be a positive number"})
            return
        if not payload.get("state"):
            self._send_json(400, {"error": "state is required"})
            return
        if safe_float(payload.get("savings_goal")) < 0:
            self._send_json(400, {"error": "savings_goal cannot be negative"})
            return

        try:
            result = analyze_profile(payload)
        except FileNotFoundError as exc:
            self._send_json(500, {"error": str(exc)})
            return
        except Exception as exc:
            self._send_json(500, {"error": f"Failed to analyze profile: {exc}"})
            return

        self._send_json(200, result)


def main() -> int:
    server = ThreadingHTTPServer(("0.0.0.0", DEFAULT_PORT), FinancialAnalysisHandler)
    print(f"Kyouth backend listening on http://localhost:{DEFAULT_PORT}")
    print(f"Dataset: {SUMMARY_PATH}")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nShutting down...")
    finally:
        server.server_close()
    return 0


