@lru_cache(maxsize=1)
def load_summary() -> dict[str, Any]:
    if not SUMMARY_PATH.exists():
        raise FileNotFoundError(
            f"Missing processed dataset: {SUMMARY_PATH}. "
            "Run scripts/etl_pipeline.py first."
        )
    with SUMMARY_PATH.open(encoding="utf-8") as handle:
        return json.load(handle)


@lru_cache(maxsize=1)
def load_expenditure_benchmarks() -> dict[str, Any]:
    """Load DOSM Table 1.8 benchmarks generated by scripts/etl_expenditure.py.

    Falls back to empty dict so the pipeline degrades gracefully if the
    ETL has not been run yet.
    """
    if not EXPENDITURE_AGE_PATH.exists():
        return {}
    with EXPENDITURE_AGE_PATH.open(encoding="utf-8") as handle:
        return json.load(handle)


def get_age_benchmark(age_group: str = "25_34") -> dict[str, float]:
    """Return RM benchmarks for a given age group from the ETL output."""
    return load_expenditure_benchmarks().get("benchmarks", {}).get(age_group, {})


def normalize_state_name(value: str | None) -> str:
    if not value:
        return "Selangor"
    cleaned = " ".join(value.strip().split())
    return STATE_ALIASES.get(cleaned.lower(), cleaned)


def safe_float(value: Any, default: float = 0.0) -> float:
    try:
        if value in (None, ""):
            return default
        return float(value)
    except (TypeError, ValueError):
        return default


def clamp(value: float, minimum: float = 0.0, maximum: float = 100.0) -> float:
    return max(minimum, min(maximum, value))


def round_currency(value: float) -> float:
    return round(max(value, 0.0), 2)


def get_state_benchmark(state: str) -> dict[str, Any]:
    summary = load_summary()
    normalized_state = normalize_state_name(state)
    for row in summary.get("state_summary", []):
        if normalize_state_name(row.get("state")) == normalized_state:
            return row
    first_date = summary.get("date_summary", [{}])
    fallback_row = first_date[0] if first_date else {}
    return {
        "state": normalized_state,
        "avg_income_mean":       fallback_row.get("avg_income_mean", 0),
        "avg_income_median":     fallback_row.get("avg_income_mean", 0),
        "avg_expenditure_mean":  fallback_row.get("avg_expenditure_mean", 0),
        "avg_poverty":           fallback_row.get("avg_poverty", 0),
    }


def sum_dynamic_list(items: list) -> float:
    """Sum amount fields from the new dynamic commitment/spending arrays."""
    return round_currency(sum(safe_float(item.get("amount")) for item in items))


def estimate_deductions(gross_salary: float) -> dict[str, float]:
    epf   = round_currency(gross_salary * 0.11)
    socso = round_currency(min(gross_salary * 0.0035, 19.75))
    eis   = round_currency(gross_salary * 0.002)
    pcb   = round_currency(max((gross_salary - 5000) * 0.03, 0.0))
    total = round_currency(epf + socso + eis + pcb)
    return {
        "epf": epf, "socso": socso, "eis": eis, "pcb": pcb,
        "total": total,
        "take_home": round_currency(gross_salary - total),
    }


def build_risk_flags(take_home: float, commitments: float, spending: float, remaining: float) -> list[str]:
    if take_home <= 0:
        return ["No take-home pay detected"]
    flags: list[str] = []
    if commitments / take_home > 0.5:
        flags.append("Fixed commitments exceed 50% of take-home pay")
    if spending / take_home > 0.35:
        flags.append("Variable spending is above a comfortable range")
    if remaining / take_home < 0.15:
        flags.append("Savings buffer is below the 15% target")
    return flags


def severity_from_ratio(ratio: float) -> str:
    if ratio >= 0.35: return "high"
    if ratio >= 0.2:  return "medium"
    return "low"


def detect_anomalies(all_items: list[dict]) -> list[dict]:
    """Compare each spending/commitment item against DOSM 25-34 age group benchmark.

    Works across both fixed commitments (Section B) and variable spending (Section C)
    so utilities, insurance etc. are benchmarked alongside food and transport.
    Benchmarks are loaded from data/processed/expenditure_by_age.json (ETL output).
    """
    benchmark_25_34 = get_age_benchmark("25_34")
    if not benchmark_25_34:
        return []   # ETL not run yet — degrade gracefully

    anomalies = []
    for item in all_items:
        label     = (item.get("label") or "").lower().strip()
        amount    = safe_float(item.get("amount"))
        bench_key = CATEGORY_TO_BENCHMARK.get(label)
        if not bench_key:
            continue
        benchmark_val = benchmark_25_34.get(bench_key, 0)
        if benchmark_val <= 0:
            continue
        ratio = amount / benchmark_val
        if ratio >= 1.20:   # flag if 20%+ above benchmark
            pct_over = round((ratio - 1) * 100)
            anomalies.append({
                "category":    item.get("label"),
                "your_amount": amount,
                "benchmark":   benchmark_val,
                "pct_over":    pct_over,
                "flag":        f"{pct_over}% above 25-34 avg",
                "severity":    "high" if ratio >= 1.5 else "medium",
            })
    return anomalies


def build_finance_gaps(snapshot: dict[str, Any], benchmark: dict[str, Any], plan: dict[str, Any]) -> list[dict[str, Any]]:
    take_home        = snapshot["take_home"]
    median_income    = safe_float(benchmark.get("avg_income_median"), 0.0)
    benchmark_spending = safe_float(benchmark.get("avg_expenditure_mean"), 0.0)
    savings_goal     = safe_float(snapshot.get("savings_goal"), 0.0)
    target_months    = int(safe_float(snapshot.get("goal_months"), 12)) or 12

    gaps: list[dict[str, Any]] = []

    income_gap_amount = max(median_income - snapshot["gross_salary"], 0.0)
    if income_gap_amount > 0:
        income_gap_ratio = income_gap_amount / median_income if median_income else 0.0
        gaps.append({
            "type": "income_gap", "severity": severity_from_ratio(income_gap_ratio),
            "amount": round_currency(income_gap_amount),
            "message": f"Income is RM {income_gap_amount:,.2f} below the {snapshot['state']} median.",
            "driver": "gross_salary",
        })

    commitment_ratio = snapshot["total_commitments"] / take_home if take_home else 0.0
    if take_home > 0 and commitment_ratio > 0.5:
        gaps.append({
            "type": "cashflow_gap", "severity": severity_from_ratio(commitment_ratio - 0.5),
            "amount": round_currency(snapshot["total_commitments"] - take_home * 0.5),
            "message": "Fixed commitments are consuming too much of take-home pay.",
            "driver": "total_commitments",
        })

    spending_gap_amount = max(snapshot["total_spending"] - benchmark_spending, 0.0)
    if spending_gap_amount > 0:
        spending_gap_ratio = spending_gap_amount / benchmark_spending if benchmark_spending else 0.0
        gaps.append({
            "type": "spending_gap", "severity": severity_from_ratio(spending_gap_ratio),
            "amount": round_currency(spending_gap_amount),
            "message": f"Variable spending is RM {spending_gap_amount:,.2f} above the benchmark average.",
            "driver": "total_spending",
        })

    if savings_goal:
        savings_gap_amount = max((savings_goal / target_months) - snapshot["remaining"], 0.0)
        if savings_gap_amount > 0:
            target_monthly    = savings_goal / target_months if target_months else 0.0
            savings_gap_ratio = savings_gap_amount / target_monthly if target_monthly else 0.0
            gaps.append({
                "type": "savings_gap", "severity": severity_from_ratio(savings_gap_ratio),
                "amount": round_currency(savings_gap_amount),
                "message": "Current leftover cash is not enough to hit the savings goal on time.",
                "driver": "remaining_cash",
            })

    if snapshot["remaining"] <= 0:
        gaps.append({
            "type": "negative_cashflow_gap", "severity": "high",
            "amount": round_currency(abs(snapshot["remaining"])),
            "message": "Monthly cash flow is negative after commitments and spending.",
            "driver": "remaining",
        })

    if not gaps:
        gaps.append({
            "type": "stable_profile", "severity": "low", "amount": 0.0,
            "message": "No critical finance gap detected against the selected benchmark.",
            "driver": "overall_profile",
        })

    return gaps


def build_snapshot(payload: dict[str, Any]) -> tuple[dict[str, Any], dict[str, Any]]:
    state     = normalize_state_name(payload.get("state"))
    benchmark = get_state_benchmark(state)

    gross_salary = round_currency(safe_float(payload.get("gross_salary")))
    deductions   = estimate_deductions(gross_salary)

    # ── FIX: respect dynamic lists, fall back to flat fields ──────────────────
    fixed_list    = payload.get("fixed_commitments", [])
    variable_list = payload.get("variable_spendings", [])

    commitments = (
        sum_dynamic_list(fixed_list) if fixed_list
        else round_currency(
            safe_float(payload.get("rent")) + safe_float(payload.get("study_loan")) +
            safe_float(payload.get("car_loan")) + safe_float(payload.get("phone_bill"))
        )
    )
    spending = (
        sum_dynamic_list(variable_list) if variable_list
        else round_currency(
            safe_float(payload.get("food_spending")) + safe_float(payload.get("transport_spending")) +
            safe_float(payload.get("entertainment"))
        )
    )

    remaining    = round_currency(deductions["take_home"] - commitments - spending)
    median_income = safe_float(benchmark.get("avg_income_median"), 0.0)
    diff_from_median = gross_salary - median_income
    diff_percent = round(abs(diff_from_median / median_income) * 100) if median_income else 0

    # ── Pre-compute anomalies (Python-driven, uses DOSM data) ────────────────
    anomalies = detect_anomalies(fixed_list + variable_list)

    # ── Build bill context lines for Gemini (AI will do the audit reasoning) ─
    phone_provider = (payload.get("phone_provider") or "").strip()
    bill_items = [
        i for i in fixed_list
        if any(k in (i.get("label") or "").lower() for k in ("phone", "wifi", "internet", "broadband", "insurance", "subscription"))
    ]
    if bill_items:
        bill_context_lines = "\n".join(
            f"  - {i.get('label')}: RM {i.get('amount')}/month"
            + (f", provider: {phone_provider}" if phone_provider else " (provider not specified)")
            for i in bill_items
        )
    else:
        bill_context_lines = "  - none declared"

    snapshot = {
        "name":              payload.get("name", "there"),
        "state":             state,
        "gross_salary":      gross_salary,
        "take_home":         deductions["take_home"],
        "epf":               deductions["epf"],
        "socso":             deductions["socso"],
        "eis":               deductions["eis"],
        "pcb":               deductions["pcb"],
        "total_commitments": commitments,
        "total_spending":    spending,
        "remaining":         remaining,
        # ── FIX: store breakdowns so build_recommendations can use them ───────
        "food_spending":     sum(safe_float(i.get("amount")) for i in variable_list if "food" in i.get("label","").lower() or "grocer" in i.get("label","").lower()) or safe_float(payload.get("food_spending")),
        "transport_spending":sum(safe_float(i.get("amount")) for i in variable_list if "transport" in i.get("label","").lower() or "grab" in i.get("label","").lower()) or safe_float(payload.get("transport_spending")),
        "entertainment":     sum(safe_float(i.get("amount")) for i in variable_list if "entertain" in i.get("label","").lower() or "shopping" in i.get("label","").lower()) or safe_float(payload.get("entertainment")),
        "fixed_commitments": fixed_list,
        "variable_spendings":variable_list,
        "anomalies":         anomalies,
        "bill_context_lines":bill_context_lines,
        "benchmark": {
            "median_income":    median_income,
            "mean_expenditure": safe_float(benchmark.get("avg_expenditure_mean"), 0.0),
            "poverty_rate":     safe_float(benchmark.get("avg_poverty"), 0.0),
        },
        "benchmark_comparison": (
            f"Your salary is {diff_percent}% above the {state} median income."
            if diff_from_median >= 0
            else f"Your salary is {diff_percent}% below the {state} median income."
        ),
        "risk_flags": build_risk_flags(deductions["take_home"], commitments, spending, remaining),
    }

    return snapshot, benchmark


def health_score_python(snapshot: dict[str, Any], benchmark: dict[str, Any]) -> tuple[int, str]:
    """Python-calculated fallback score — used only when Gemini fails."""
    take_home = snapshot["take_home"]
    if take_home <= 0:
        return 0, "Critical"

    remaining_ratio  = snapshot["remaining"] / take_home
    commitment_ratio = snapshot["total_commitments"] / take_home
    spending_ratio   = snapshot["total_spending"] / take_home
    benchmark_gap    = snapshot["gross_salary"] - safe_float(benchmark.get("avg_income_median"), 0.0)

    score = 55
    score += remaining_ratio * 45
    score -= max(commitment_ratio - 0.35, 0) * 70
    score -= max(spending_ratio  - 0.25, 0) * 35
    if benchmark_gap > 0:
        score += 5

    score = int(clamp(score))
    label = "Strong" if score >= 75 else ("Needs Attention" if score >= 55 else "At Risk")
    return score, label


def build_plan(snapshot: dict[str, Any], benchmark: dict[str, Any], payload: dict[str, Any]) -> dict[str, Any]:
    """Generates a financial plan — single Gemini call returning all 6 AI features as structured JSON."""

    # ── STEP 1: Base values ────────────────────────────────────────────────────
    take_home         = snapshot.get("take_home", 0)
    total_commitments = snapshot.get("total_commitments", 0)
    total_spending    = snapshot.get("total_spending", 0)
    remaining         = max(snapshot.get("remaining", 0), 0)
    goal_amount       = safe_float(payload.get("savings_goal"), 0)
    target_months     = int(safe_float(payload.get("goal_months"), 6)) or 6

    # ── STEP 2: Goal-driven maths ──────────────────────────────────────────────
    monthly_needed   = round(goal_amount / target_months, 2) if goal_amount and target_months else 0
    monthly_saveable = remaining
    total_saveable   = round(monthly_saveable * target_months, 2)
    is_achievable    = total_saveable >= goal_amount if goal_amount > 0 else True
    actual_months    = math.ceil(goal_amount / monthly_saveable) if monthly_saveable > 0 and goal_amount > 0 else None

    # ── STEP 3: Budget allocation ──────────────────────────────────────────────
    needs_pct       = round(total_commitments / take_home, 4) if take_home else 0
    raw_savings_pct = monthly_needed / take_home if take_home else 0.20
    savings_pct     = max(0.20, min(raw_savings_pct, 0.60))
    wants_pct       = max(round(1 - needs_pct - savings_pct, 4), 0.05)
    if wants_pct == 0.05:
        savings_pct = round(1 - needs_pct - 0.05, 4)

    needs_amount   = total_commitments
    savings_amount = min(round(take_home * savings_pct, 2), remaining)
    savings_pct    = round(savings_amount / take_home, 4) if take_home else savings_pct
    wants_amount   = round(take_home - needs_amount - savings_amount, 2)
    wants_pct      = round(wants_amount / take_home, 4) if take_home else wants_pct
    wants_remaining = round(wants_amount - total_spending, 2)

    # ── STEP 4: Savings projection ─────────────────────────────────────────────
    savings_projection = [
        {
            "month": f"M{i}",
            "projected_savings": round(monthly_saveable * i, 2),
            "needed_savings":    round(monthly_needed * i, 2),
            "goal_amount":       goal_amount,
        }
        for i in range(1, target_months + 1)
    ]

    # ── STEP 5: Goal status text ───────────────────────────────────────────────
    if goal_amount <= 0:
        goal_status = "Enter a savings goal to see your projection."
    elif monthly_saveable <= 0:
        goal_status = "Your cash flow is negative. Reduce expenses before targeting a savings goal."
    elif not is_achievable:
        shortfall       = round(goal_amount - total_saveable, 2)
        suggested_goal  = round(monthly_saveable * target_months, -2)
        goal_status = (
            f"Your goal of RM {goal_amount:,.0f} is not achievable in {target_months} months. "
            f"Saving all your remaining cash (RM {monthly_saveable:,.2f}/month), "
            f"you can only reach RM {total_saveable:,.0f} — short by RM {shortfall:,.0f}. "
            f"Realistic options: extend to {actual_months} months, or adjust your goal to RM {suggested_goal:,.0f}."
        )
    elif actual_months and actual_months <= target_months:
        months_ahead = target_months - actual_months
        goal_status = (
            f"You are on track to reach RM {goal_amount:,.0f} in {actual_months} months"
            f"{f' — {months_ahead} months ahead of your {target_months}-month target.' if months_ahead > 0 else '.'}"
        )
    else:
        shortfall_pm = round(monthly_needed - monthly_saveable, 2)
        goal_status = (
            f"At your current pace you will reach RM {goal_amount:,.0f} in {actual_months} months, "
            f"but your target is {target_months} months. "
            f"Save an extra RM {shortfall_pm:,.2f}/month to hit your target on time."
        )

    # ── STEP 6: Prepare context strings for Gemini ────────────────────────────
    spending_lines = "\n".join(
        f"  - {item['label']}: RM {item['amount']}"
        for item in snapshot.get("variable_spendings", [])
    ) or "  - (no itemised spending provided)"

    commitment_lines = "\n".join(
        f"  - {item['label']}: RM {item['amount']}"
        for item in snapshot.get("fixed_commitments", [])
    ) or "  - (no itemised commitments provided)"

    benchmark_25_34 = get_age_benchmark("25_34")
    utilities_benchmark = benchmark_25_34.get("housing_utilities", 1201.69)

    anomaly_lines = "\n".join(
        f"  - {a['category']}: RM {a['your_amount']} vs DOSM 25-34 benchmark RM {a['benchmark']} ({a['flag']})"
        for a in snapshot.get("anomalies", [])
    ) or "  - none detected"

    bill_context_lines = snapshot.get("bill_context_lines", "  - none declared")

    median_income = safe_float(benchmark.get("avg_income_median"), 0.0)
    salary_gap    = round(max(median_income - snapshot["gross_salary"], 0), 2)

